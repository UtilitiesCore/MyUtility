using System;
using System.Collections.Generic;
using System.Text;

namespace MyUtility.Commons.NumberConvert
{
    public interface INumberConvert
    {
        /// <summary>
        /// 数字转换为指定的进制形式字符串
        /// </summary>
        /// <param name="number"></param>
        /// <returns></returns>
        string ToString(long number);

        /// <summary>
        /// 指定字符串转换为指定进制的数字形式
        /// </summary>
        /// <param name="str"></param>
        /// <returns></returns>
        long FromString(string str);
    }
}
